<template>
  <div class="inputBox shadow">
      <input type="text" v-model="newTodoItem" v-on:keyup.enter="addTodo">   <!-- v-model로 data 값 연동 -->
      <span class="addContainer" v-on:click="addTodo">
          <i class="fas fa-plus addBtn"></i>
      </span>
  </div>
</template>

<script>
export default {
    data: function() {
        return {
            newTodoItem: ""
        }
    },
    methods: {
        addTodo: function() {   // 저장만 하는 기능
            if(this.newTodoItem !== '') {
                var obj = {completed: false, item: this.newTodoItem};   // completed를 list에서 사용하기 위해 이런 식으로 한다.
                localStorage.setItem(this.newTodoItem, JSON.stringify(obj));    // Object를 String으로 변환해서 저장한다.
                this.clearInput();
                // 개발자모드 appliciton에 가면 localStorage 이곳에 key : value로 저장된다.
            }
        },                                                  // 기능을 따로 분할해서 설정
        clearInput: function() {    // 지우는 기능
            this.newTodoItem = '';
        }
    }
}
</script>

